#!/usr/bin/env python3

"""
This module a function `to_str` which a float to its string representation.
It demonstrates the use of type annotations and string conversion in Python.
"""


def to_str(n: float) -> str:
    """
    Converts a float to its string representation.

    This function takes a floating-point number and returns its string
    representation.

    Parameters:
    n (float): The floating-point number to convert.

    Returns:
    str: The string representation of the input float.
    """
    return str(n)
